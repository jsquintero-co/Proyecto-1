--Drops de tablas 
DROP TABLE PARTIDO CASCADE CONSTRAINTS;
DROP TABLE SELECCION CASCADE CONSTRAINTS;
DROP TABLE CONFEDERACION CASCADE CONSTRAINTS;
DROP TABLE ESTADIO CASCADE CONSTRAINTS;
DROP TABLE CIUDAD CASCADE CONSTRAINTS;
DROP TABLE PAIS CASCADE CONSTRAINTS;

--Creación de tablas para la base de datos del Mundial de Fútbol
CREATE TABLE PAIS (
    ID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    NOMBRE_PAIS VARCHAR2(100) UNIQUE NOT NULL
);

CREATE TABLE CIUDAD (
    ID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    NOMBRE_CIUDAD VARCHAR2(100) UNIQUE NOT NULL,
    ID_PAIS NUMBER NOT NULL,
    
    CONSTRAINT FK_CIUDAD_PAIS 
    FOREIGN KEY (ID_PAIS) REFERENCES PAIS(ID)
);

CREATE TABLE ESTADIO (
    ID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    NOMBRE_ESTADIO VARCHAR2(100) UNIQUE NOT NULL,
    CAPACIDAD NUMBER DEFAULT 0,
    ID_CIUDAD NUMBER NOT NULL,

    CONSTRAINT FK_ESTADIO_CIUDAD 
    FOREIGN KEY (ID_CIUDAD) REFERENCES CIUDAD(ID)
);

CREATE TABLE CONFEDERACION (
    ID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    NOMBRE_CONFEDERACION VARCHAR2(100) UNIQUE NOT NULL
);

CREATE TABLE SELECCION (
    ID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    NOMBRE_SELECCION VARCHAR2(100) UNIQUE NOT NULL,
    ID_CONFEDERACION NUMBER NOT NULL,

    CONSTRAINT FK_SELECCION_CONF 
    FOREIGN KEY (ID_CONFEDERACION) REFERENCES CONFEDERACION(ID)
);

CREATE TABLE PARTIDO (
    ID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    ID_ESTADIO NUMBER NOT NULL,
    FECHA DATE DEFAULT SYSDATE,
    FASE VARCHAR2(20),
    ID_SELECCION_LOCAL NUMBER NOT NULL,
    ID_SELECCION_VISITANTE NUMBER NOT NULL,
    GOLES_LOCAL NUMBER DEFAULT 0,
    GOLES_VISITANTE NUMBER DEFAULT 0,

    CONSTRAINT FK_PARTIDO_ESTADIO 
    FOREIGN KEY (ID_ESTADIO) REFERENCES ESTADIO(ID),

    CONSTRAINT FK_PARTIDO_LOCAL 
    FOREIGN KEY (ID_SELECCION_LOCAL) REFERENCES SELECCION(ID),

    CONSTRAINT FK_PARTIDO_VISITANTE 
    FOREIGN KEY (ID_SELECCION_VISITANTE) REFERENCES SELECCION(ID),

    -- Restricción: fases válidas
    CONSTRAINT CK_FASE 
    CHECK (FASE IN ('GRUPOS', 'OCTAVOS', 'CUARTOS', 'SEMIFINAL', 'FINAL')),

    -- Restricción: no jugar contra sí mismo
    CONSTRAINT CK_EQUIPOS_DISTINTOS 
    CHECK (ID_SELECCION_LOCAL <> ID_SELECCION_VISITANTE)
);